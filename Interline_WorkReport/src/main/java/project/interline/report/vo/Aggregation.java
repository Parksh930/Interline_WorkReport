package project.interline.report.vo;

public class Aggregation {
	String userNum;
	String userName;
	String position;
	String team;
	String yearMonth;
	String absent;
	String vacation;
	int holidayWorkingDay;
	String sumWorkingTime;
	String meanWorkingTime;
	public String getUserNum() {
		return userNum;
	}
	public void setUserNum(String userNum) {
		this.userNum = userNum;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getYearMonth() {
		return yearMonth;
	}
	public void setYearMonth(String yearMonth) {
		this.yearMonth = yearMonth;
	}
	public String getAbsent() {
		return absent;
	}
	public void setAbsent(String absent) {
		this.absent = absent;
	}
	public String getVacation() {
		return vacation;
	}
	public void setVacation(String vacation) {
		this.vacation = vacation;
	}
	public int getHolidayWorkingDay() {
		return holidayWorkingDay;
	}
	public void setHolidayWorkingDay(int holidayWorkingDay) {
		this.holidayWorkingDay = holidayWorkingDay;
	}
	public String getSumWorkingTime() {
		return sumWorkingTime;
	}
	public void setSumWorkingTime(String sumWorkingTime) {
		this.sumWorkingTime = sumWorkingTime;
	}
	public String getMeanWorkingTime() {
		return meanWorkingTime;
	}
	public void setMeanWorkingTime(String meanWorkingTime) {
		this.meanWorkingTime = meanWorkingTime;
	}
}
